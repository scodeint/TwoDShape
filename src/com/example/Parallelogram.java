package com.example;

public class Parallelogram {
    public static void area(int b, int h){
        int area;
        area = b*h;
        System.out.println(area);
    }
    public static void perimeter(int b, int h){
        int perimeter;
        perimeter = 2*(b*h);
        System.out.println(perimeter);
    }
}
