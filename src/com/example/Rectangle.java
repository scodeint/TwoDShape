package com.example;

public class Rectangle {
    public static void areaOfRectangle(int b, int h){
        int area;
        area = b*h;
       System.out.println(area);
    }
    public static void perimeterOfRectangle(int l, int b){
        int perimeter;
        perimeter = 2*(l+b);
        System.out.println(perimeter);
    }
}
