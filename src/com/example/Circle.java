package com.example;

public class Circle {
    public static void area(double r){
        double area;
        area = 3.143*(r*r);
        System.out.println("Area of circle ="+" "+area);
    }
    public static void perimeter(double r){
        double perimeter;
        perimeter = 2*(3.143*r);
        System.out.println("The perimeter of the circle="+" "+perimeter);
    }
}
